list_ = [8, 9, -5, -3, 1, -10, 8, -10, -5, 0, 5, -4, 0, 10, -8, 1, 6, -6, 6, -9]

set_ = set(list_)   # Множество list_ или уже кол-во уникальных чисел
sum_ = sum(set_)    # Сумма уникальных чисел
len_ = len(set_)   # Количество уникальных чисел
average = round((sum_ / len_), 5)   # Среднее арифм. уникальных чисел

print(sum_)
print(len_)
print(average)
